    public void comprobar() {

        String nombre = txtUsuario.getText().trim();
        String pass = txtClave.getText().trim();
        String key = "92AE31A79FEEB2A3"; //llave
        String iv = "0123456789ABCDEF"; // vector de inicializaci√≥n
        String clavecifrado = null; //variable para encriptar

        if (nombre.length() == 0 || pass.length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingresar datos!");
            txtUsuario.requestFocus();
            return;
        }

        try {
            sql = "select clave from usuarios where nombre = '" + nombre + "'";
        
            con = cn.getConnection();
            st = con.createStatement();
            rs = st.executeQuery(sql);

            if (rs.next()) {

                clavecifrado = encrypt.decrypt(key, iv, rs.getString("clave"));
                
                if (pass.equals(String.valueOf(clavecifrado))) {

                    ViewMDIPrincipal mdi = new ViewMDIPrincipal();
                    mdi.setVisible(true);
                    dispose();

                } else {

                    JOptionPane.showMessageDialog(this, "Clave incorrecta!");
                    inicializar();

                }
            }
        } catch (Exception e) {
        }
    }

    public void inicializar() {
        txtUsuario.setText(null);
        txtClave.setText(null);
        txtUsuario.requestFocus();
    }

